package com.asgteach.familytree.eventnew;

import com.asgteach.familytree.model.Event;
import com.asgteach.familytree.model.Person;

/**
 *
 * @author gail
 */
public class EventInfo {
    private boolean isValid;
        private String message;
        private Event event;
        private Person oldValue;
        private Person newValue;

        public boolean isIsValid() {
            return isValid;
        }

        public void setIsValid(boolean isValid) {
            this.isValid = isValid;
        }

        public String getMessage() {
            return message;
        }

        public void setMessage(String message) {
            this.message = message;
        }

        public Event getEvent() {
            return event;
        }

        public void setEvent(Event event) {
            this.event = event;
        }

    public Person getOldValue() {
        return oldValue;
    }

    public void setOldValue(Person oldValue) {
        this.oldValue = oldValue;
    }

    public Person getNewValue() {
        return newValue;
    }

    public void setNewValue(Person newValue) {
        this.newValue = newValue;
    }        
    
}
